void load() {

        DefaultTableModel model = (DefaultTableModel) tblCTHD2.getModel();
        model.setRowCount(0);

        List<ChiTietHoaDon> list = hd.select();

        try {

            for (ChiTietHoaDon cd : list) {
                Object[] row = {
                    cd.getMaChiTietHD(),
                    cd.getMaHD(),
                    cd.getMaSP(),
                    cd.getSoLuong(),
                    cd.getThanhTien(),
                    cd.getKichThuoc()
                };
                model.addRow(row);
            }
        } catch (Exception e) {
            e.printStackTrace();
            DialogHelper.alert(this, "Lỗi truy vấn dữ liệu!");
        }

    }

